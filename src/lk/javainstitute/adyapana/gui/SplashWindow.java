    private void loadAnimation() {
        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = 0; i <= 100; i++) {

                    jProgressBar1.setValue(i);
                    try {
                        Thread.sleep(40);
                    } catch (Exception e) {
                       logger.log(java.util.logging.Level.WARNING, "Exception Occured", e);
                    }
                }

                splashWindow.dispose();
                new SystemLogin().setVisible(true);

            }
        });

        t.start();
    }

